int	ft_sqrt(int nb)
{
	int	half;
	int	i;

	if (nb == 1)
		return (1);
	half = nb / 2;
	i = 0;
	if (nb > 0)
	{
		while (i <= half)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
