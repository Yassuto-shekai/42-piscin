int	ft_str_is_numeric(char *str)
{
	int	m;

	m = 0;
	while (str[m] != '\0')
	{
		if (str[m] >= '0' && str[m] <= '9')
		{
			m++;
		}
		else
			return (0);
	}
	return (1);
}
