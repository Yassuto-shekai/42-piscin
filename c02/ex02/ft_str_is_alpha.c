int	ft_str_is_alpha(char *str)
{
	int	m;

	m = 0;
	while (str[m] != '\0')
	{
		if (str[m] >= 'a' && str[m] <= 'z')
		{
			m++;
		}
		else if (str[m] >= 'A' && str[m] <= 'Z')
		{
			m++;
		}
		else
			return (0);
	}
	return (1);
}
